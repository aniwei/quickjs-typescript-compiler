/**
 * 定义了 QuickJS 虚拟机的一部分操作码 (Opcode)。
 * 命名和值都来源于 quickjs.c 源码。
 * 这是编译器后端生成字节码的基础。
 */
export const OP = {
  // 将一个 32-bit 整数压入操作数栈
  // 后面跟4个字节的立即数 (integer)
  push_i32: 0x01,

  // 丢弃栈顶的值
  drop: 0x05,

  // 从当前函数返回，并将栈顶的值作为返回值
  ret: 0x08,

  // 从栈顶弹出两个值，相加后将结果压栈
  add: 0x0b,

  // 从栈顶弹出两个值，相减后将结果压栈
  sub: 0x0c,

  // 从栈顶弹出两个值，相乘后将结果压栈
  mul: 0x0d,

  // 从栈顶弹出两个值，相除后将结果压栈
  div: 0x0e,
};